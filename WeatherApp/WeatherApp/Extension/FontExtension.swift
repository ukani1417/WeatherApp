import Foundation
import UIKit

extension UIFont {
    static func robotoSlabMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "RobotoSlab-Medium", size: size)
    }
    static func robotoSlabLight(size: CGFloat) -> UIFont? {
        return UIFont(name: "RobotoSlab-Light", size: size)
    }
}
